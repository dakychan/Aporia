package dev.anidumpproject.api.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Аннотация для нативной защиты кода через VMProtect.
 * <p>
 * Маркирует методы и классы для применения различных уровней обфускации
 * и защиты от реверс-инжиниринга.
 * </p>
 * 
 * <h3>Типы защиты:</h3>
 * <ul>
 *   <li><b>STANDARD</b> - Базовая защита без обфускации</li>
 *   <li><b>VMProtectBeginMutation</b> - Мутация кода (низкое влияние на производительность)</li>
 *   <li><b>VMProtectBeginUltra</b> - Комбинированная защита (рекомендуется)</li>
 *   <li><b>VMProtectBeginVirtualization</b> - Виртуализация кода (высокое влияние на FPS)</li>
 * </ul>
 * 
 * <h3>Примеры использования:</h3>
 * <pre>{@code
 * // Защита метода с максимальной защитой
 * @Native(type = Native.Type.VMProtectBeginUltra)
 * public void criticalMethod() {
 *     // Критичный код
 * }
 * 
 * // Защита класса целиком
 * @Native(type = Native.Type.VMProtectBeginMutation)
 * public class SecureClass {
 *     // Все методы будут защищены
 * }
 * 
 * // Защита с приоритетом
 * @Native(
 *     type = Native.Type.VMProtectBeginUltra,
 *     priority = Priority.CRITICAL
 * )
 * public void licenseCheck() {
 *     // Проверка лицензии
 * }
 * }</pre>
 * 
 * <h3>Рекомендации:</h3>
 * <ul>
 *   <li>Используйте <b>VMProtectBeginUltra</b> для критичных методов (лицензия, античит)</li>
 *   <li>Используйте <b>VMProtectBeginMutation</b> для обычных методов</li>
 *   <li>Избегайте <b>VMProtectBeginVirtualization</b> в горячих путях (рендеринг, тики)</li>
 *   <li>Не защищайте геттеры/сеттеры и простые утилиты</li>
 * </ul>
 * 
 * <h3>Влияние на производительность:</h3>
 * <table border="1">
 *   <tr>
 *     <th>Тип</th>
 *     <th>Защита</th>
 *     <th>Оверхед</th>
 *     <th>Применение</th>
 *   </tr>
 *   <tr>
 *     <td>STANDARD</td>
 *     <td>Низкая</td>
 *     <td>~0%</td>
 *     <td>Публичные API</td>
 *   </tr>
 *   <tr>
 *     <td>Mutation</td>
 *     <td>Средняя</td>
 *     <td>~5%</td>
 *     <td>Обычные методы</td>
 *   </tr>
 *   <tr>
 *     <td>Ultra</td>
 *     <td>Высокая</td>
 *     <td>~15%</td>
 *     <td>Критичные методы</td>
 *   </tr>
 *   <tr>
 *     <td>Virtualization</td>
 *     <td>Максимальная</td>
 *     <td>~50%</td>
 *     <td>Только для лицензии</td>
 *   </tr>
 * </table>
 * 
 * @author protect3ed
 * @version 2.0
 * @since 1.0
 * @see MainClass
 */
@Retention(RetentionPolicy.CLASS)
@Target({ElementType.METHOD, ElementType.TYPE, ElementType.CONSTRUCTOR})
public @interface Native {

    /**
     * Тип нативной защиты.
     * <p>
     * Определяет уровень обфускации и защиты кода.
     * </p>
     * 
     * @return тип защиты (по умолчанию STANDARD)
     */
    Type type() default Type.STANDARD;
    
    /**
     * Приоритет защиты.
     * <p>
     * Используется для определения порядка применения защиты
     * при наличии конфликтов или ограничений ресурсов.
     * </p>
     * 
     * @return приоритет (по умолчанию NORMAL)
     */
    Priority priority() default Priority.NORMAL;
    
    /**
     * Комментарий для разработчика.
     * <p>
     * Описывает причину применения защиты к данному методу/классу.
     * Используется для документирования и аудита безопасности.
     * </p>
     * 
     * @return комментарий
     */
    String comment() default "";
    
    /**
     * Включить дополнительную защиту от отладки.
     * <p>
     * Добавляет проверки на наличие отладчика и эмуляторов.
     * Может снизить производительность на ~5%.
     * </p>
     * 
     * @return true если нужна анти-дебаг защита
     */
    boolean antiDebug() default false;
    
    /**
     * Включить защиту от дампа памяти.
     * <p>
     * Шифрует чувствительные данные в памяти.
     * Рекомендуется для методов работающих с паролями и ключами.
     * </p>
     * 
     * @return true если нужна защита от дампа
     */
    boolean antiDump() default false;

    /**
     * Типы нативной защиты VMProtect.
     */
    enum Type {
        /**
         * Стандартная защита без обфускации.
         * <p>
         * Используется для публичных API и методов без чувствительной логики.
         * Не влияет на производительность.
         * </p>
         */
        STANDARD,
        
        /**
         * Мутация кода - изменение инструкций процессора.
         * <p>
         * Заменяет стандартные инструкции на эквивалентные последовательности.
         * Усложняет статический анализ при минимальном влиянии на производительность.
         * </p>
         * <ul>
         *   <li>Оверхед: ~5%</li>
         *   <li>Защита: Средняя</li>
         *   <li>Применение: Обычные методы, конфиги, сетевые запросы</li>
         * </ul>
         */
        VMProtectBeginMutation,
        
        /**
         * Комбинированная защита - мутация + частичная виртуализация.
         * <p>
         * Золотой стандарт защиты. Комбинирует мутацию с выборочной
         * виртуализацией критичных участков кода.
         * </p>
         * <ul>
         *   <li>Оверхед: ~15%</li>
         *   <li>Защита: Высокая</li>
         *   <li>Применение: Критичные методы, античит, проверка лицензий</li>
         * </ul>
         */
        VMProtectBeginUltra,
        
        /**
         * Виртуализация кода - выполнение в виртуальной машине.
         * <p>
         * Максимальная защита. Код выполняется в собственной виртуальной машине
         * с уникальным набором инструкций. Сильно влияет на производительность.
         * </p>
         * <ul>
         *   <li>Оверхед: ~50%</li>
         *   <li>Защита: Максимальная</li>
         *   <li>Применение: ТОЛЬКО для критичных проверок лицензий</li>
         * </ul>
         * <p>
         * <b>ВНИМАНИЕ:</b> Не используйте в рендеринге, тиках и горячих путях!
         * Может вызвать просадки FPS.
         * </p>
         */
        VMProtectBeginVirtualization,
    }
    
    /**
     * Приоритеты защиты.
     */
    enum Priority {
        /**
         * Низкий приоритет - применяется последним.
         * Используется для некритичных методов.
         */
        LOW,
        
        /**
         * Обычный приоритет - стандартное применение.
         * Используется по умолчанию.
         */
        NORMAL,
        
        /**
         * Высокий приоритет - применяется раньше обычных.
         * Используется для важных методов.
         */
        HIGH,
        
        /**
         * Критичный приоритет - применяется первым.
         * Используется для лицензий и античита.
         */
        CRITICAL
    }
}
